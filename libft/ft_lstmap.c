#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new;
	t_list	*node;
	void	*content;

	node = NULL;
	while (lst)
	{
		content = f(lst->content);
		new = ft_lstnew(content);
		if (!new)
		{
			del(content);
			ft_lstclear(&node, del);
			return ((void *)(0));
		}
		ft_lstadd_back(&node, new);
		lst = lst->next;
	}
	new = NULL;
	return (node);
}
